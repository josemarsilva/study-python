print("algum string (str)")
print('algum string (str)')
print(1234567)

# enquote strings with double quotes or single quotes
print("algum 'string' (str)")
print('algum "string" (str)')

# escape char for quotes
print("algum \"string\" (str)")
print('algum \'string\' (str)')

# raw text
print("vou enviar \n barra ene")
print(r"vou enviar \n barra ene")